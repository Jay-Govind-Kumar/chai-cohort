/* ---------------- Challenge 3: Chocolates ---------------- */

// You're packing chocolate bars into gift boxes. Each box must contain the same number of chocolates bars. How many chocolate bars do you need for all the boxes?

// Create a fucntion that multiplies two numbers ( chocolates bars per box and boxes) and returns the total number of chocolate bars needed.

function totalChocolateBars(barsPerBox, numberOfBoxes) {
  return barsPerBox * numberOfBoxes;
}
