/* ---------------- Challenge 2: Apples ---------------- */

// You have a basket of apples, but you decide to give some away to your neighbour. How many apples are left in your basket ?

// Create a function that subtracts the number of apples given away from the total apples you started with, then returns the remaining apples.

function remainingApples(totalApples, givenAway) {
  // Return totalApples minus givenAway
  return totalApples - givenAway;
}
